#!/bin/bash

easy_install -U Sphinx

# Installation of easy_install

sudo apt-get install python-setuptools

# Installation of Sphinx

sudo easy_install -U Sphinx

sudo pip install pygments

# Installation of Sphinx for PHP Code Coloring

sudo easy_install -U sphinxcontrib-phpdomain

# In order to be able to generate PDF and PS files from reStructuredText using LaTeX on Debian-based systems you need to install the following software:

sudo apt-get install wget build-essential texlive-full

